A = input("Δώσε πίνακα Α: ");
[m, n] = size(A);
if m==n 
    x = det(A);
    if x~=0
        disp('Ο Α είναι αντιστρέψιμος');
        % συμπληρώστε ώστε να υπολογιστεί το ίχνος του πίνακα Α
        disp(trace(A));
    else
        disp('Ο Α δεν είναι αντιστρέψιμος');
        % συμπληρώστε ώστε να υπολογισθεί η ανηγμένη μορφή του πίνακα Α
        rref(A);
    end
end
